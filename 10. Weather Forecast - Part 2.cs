double a = double.Parse(Console.ReadLine());
if (a <= 11.90)
{
    Console.WriteLine("Cold");
}
else if (a >= 26.00)
{
    Console.WriteLine("Hot");
}
else if (a >= 20.10)
{
    Console.WriteLine("Warm");
}
else if (a >= 15.00)
{
    Console.WriteLine("Mild");
}
else if (a >= 12.00)
{
    Console.WriteLine("Cool");
}
